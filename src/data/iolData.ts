// Get unique manufacturers sorted alphabetically
export const getManufacturers = (): string[] => {
  const manufacturers = [...new Set(iolLenses.map(lens => lens.manufacturer))];
  return manufacturers.sort();
};

// Get IOL models for a specific manufacturer
export const getIOLsByManufacturer = (manufacturer: string): IOLLens[] => {
  return iolLenses.filter(lens => lens.manufacturer === manufacturer);
};

// Search IOLs by text query
export const searchIOLs = (query: string): IOLLens[] => {
  const lowerQuery = query.toLowerCase();
  return iolLenses.filter(lens =>
    lens.manufacturer.toLowerCase().includes(lowerQuery) ||
    lens.iol.toLowerCase().includes(lowerQuery)
  );
};
